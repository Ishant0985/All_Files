from reportlab.pdfgen import canvas
from reportlab.lib.pagesizes import A4
from reportlab.pdfbase.ttfonts import TTFont
from reportlab.pdfbase import pdfmetrics
import os

# Create PDF document
file_path = "story_in_hindi.pdf"  # Change this path if needed
c = canvas.Canvas(file_path, pagesize=A4)

# Register a Unicode font that supports Hindi
font_path = "path/to/NotoSansDevanagari-Regular.ttf"  # Replace with the path to a font file that supports Hindi, e.g., "Mangal.ttf"
pdfmetrics.registerFont(TTFont("HindiFont", font_path))

# Set the font to the registered font that supports Hindi
c.setFont("HindiFont", 12)

# Define the text to be written in the PDF (in Hindi)
text = """हेलो दोस्तों, स्वागत है आप सभी का मेरी इस नई स्टोरीटेलर की इस जर्नी में और मेरे पहले वीडियो में।  तो चलिए कहानी की शुरुआत करते हैं।

**शीर्षक: बांस पर बहस**

एक शांत गाँव में, जहाँ हरियाली से घिरे खेत थे, एक घर खड़ा था। इस घर के निर्माण में इस्तेमाल होने वाली एक लकड़ी की सीढ़ी ही दो पीढ़ियों के बीच की खटपट का कारण बन गई थी। रमेश, जो मेहनती और समझदार व्यक्ति थे, और उनके पिता, हरि राम, जो बुजुर्ग और जिद्दी स्वभाव के थे, इस सीढ़ी को लेकर आपस में उलझ पड़े।

घर के निर्माण के समय जब प्लास्टर और पेंटिंग का काम चल रहा था, तब रमेश ने यह लकड़ी की सीढ़ी बनवाई थी ताकि ऊँचाई पर काम करने में आसानी हो। यह सीढ़ी न सिर्फ घर के निर्माण के लिए बल्कि भविष्य में छोटे-मोटे कामों के लिए भी काम आने वाली थी। लेकिन दो साल बाद ही यह सीढ़ी कमजोर पड़ने लगी और दरारें आने लगीं।

हरि राम, जिन्होंने अपनी जवानी में लकड़ी और बांस का काम किया था, इसे देखकर क्रोधित हो गए। उन्होंने रमेश से कहा, "तुम्हें अच्छे और मजबूत लकड़ी की पहचान नहीं है। तुमने जो लकड़ी चुनी, वह इतनी जल्दी खराब हो गई। मैंने अगर चुनी होती तो यह सीढ़ी सालों-साल चलती।"

रमेश, जो पहले से ही जीवन की परेशानियों में घिरा था, अब और सहन नहीं कर पाया। उसने गुस्से में जवाब दिया, "मैंने जो सही समझा, वही किया। मुझे क्या पता था कि कौन सी लकड़ी अच्छी रहेगी?"

इस तरह दोनों के बीच बहस बढ़ने लगी। हरि राम अपनी पुरानी आदतों के अनुसार, हर बात में टोकते रहते थे। कोई भी काम उनकी उम्मीदों के अनुसार न हो, तो वे आलोचना करने से पीछे नहीं हटते थे। रमेश को यह सब सहते हुए कई साल हो गए थे, लेकिन अब वह भी हर छोटी बात पर भड़कने लगा था।

यह बहस सिर्फ सीढ़ी की नहीं थी, बल्कि उन सभी छोटी-बड़ी बातों की थी जो सालों से उनके बीच दबी पड़ी थीं। तब रमेश की पत्नी अंजलि, जो वर्षों से इन तकरारों को देखती आ रही थीं, ने बीच में आकर कहा, "यह बहस लकड़ी की सीढ़ी की नहीं है। यह आप दोनों की समझ और एक-दूसरे की इज्जत की है। लकड़ी कमजोर हो सकती है, लेकिन आपके रिश्ते नहीं।"

उनके शब्दों ने दोनों के दिलों को छू लिया। वे समझ गए कि असल समस्या तो उनकी एक-दूसरे की बात न समझने की थी। हरि राम अपने अनुभव से बेटे की मदद करना चाहते थे, जबकि रमेश को अपने जीवन में खुद के फैसलों का सम्मान चाहिए था।

आखिरकार, दोनों ने मिलकर फिर से एक मजबूत और टिकाऊ सीढ़ी बनाने का निर्णय लिया। इस बार हरि राम ने सही लकड़ी चुनने का तरीका सिखाया, और रमेश ने भी उनके अनुभव की कद्र की। यह सिर्फ एक सीढ़ी नहीं थी, बल्कि एक टूटते हुए रिश्ते को फिर से मजबूत करने का जरिया था।

इस तरह, "बांस पर बहस" ने उन्हें समझाया कि असली ताकत लकड़ी या पत्थर में नहीं, बल्कि एक-दूसरे की सुनने और समझने की चाहत में होती है। यहाँ कहानी समाप्त होती है।

अगर आप सभी को इस कहानी से क्या नया सीखने को मिलता है कमेंट जरूर बताएं। अगर आपको यह कहानी पसंद आयी हो तो वीडियो को लइका करके चैनल को सब्सक्राइब करदे ताकि हमें आगे आने वाले कनियो के लिए मोटिवेशन मिल सके। धन्यवाद्।
"""

# Split the text into lines and write it to the PDF
lines = text.split("\n")
textobject = c.beginText(40, 800)  # Starting position
for line in lines:
    textobject.textLine(line)
c.drawText(textobject)

# Save the PDF
c.save()

# Print the path to the created file
print(f"PDF saved at: {os.path.abspath(file_path)}")
